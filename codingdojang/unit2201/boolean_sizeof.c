#include <stdio.h>
#include <stdbool.h>

int main()
{
    printf("int의 크기: %d\n", sizeof(int));
    printf("bool의 크기: %d\n", sizeof(bool));

    bool b1 = false;
    bool b2 = true;

    if (b1 != true)
        printf("참\n");
    else
        printf("거짓\n");

    printf("%s\n", b2 == false ? "참" : "거짓");

    return 0;
}